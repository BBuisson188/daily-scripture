const normalize = (value) => value.toLowerCase().replace(/\./g, '').replace(/\s+/g, ' ').trim();

export const bookLookup = new Map();

for (const book of books) {
  bookLookup.set(normalize(book.name), book);
  for (const alias of book.aliases) {
    bookLookup.set(normalize(alias), book);
  }
}

export function findBook(rawName) {
  return bookLookup.get(normalize(rawName));
}

export function verseCountForChapter(bookName, chapter) {
  const book = findBook(bookName);
  if (!book || chapter < 1 || chapter > book.chapters.length) return null;
  return book.chapters[chapter - 1];
}
